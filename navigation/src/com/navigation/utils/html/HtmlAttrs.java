package com.navigation.utils.html;

/**
 * 包含 {@code HTML} 元素的属性名称作为常量的工具类。
 * 
 * @author Fuchun
 * @version 1.0, 2009-06-10
 */
public class HtmlAttrs {

    /** 标签唯一标识的属性名称 - {@code id}。 */
    public static final String ID             = "id";

    /** 表示标签的名字的属性名称 - {@code name}。 */
    public static final String NAME           = "name";

    /** 表示标签的 {@code CSS} 样式类的属性名称 - {@code class}。 */
    public static final String CLASSNAME      = "class";

    /** 表示标签宽度的属性名称 - {@code width}。 */
    public static final String WIDTH          = "width";

    /** 表示标签高度的属性名称 - {@code height}。 */
    public static final String HEIGHT         = "height";

    /** 表示标签标题文本的属性名称 - {@code title}。 */
    public static final String TITLE          = "title";

    /** 表示标签提示文本的属性名称 - {@code alt}。 */
    public static final String ALT            = "alt";

    /** 表示标签样式的属性名称 - {@code style}。 */
    public static final String STYLE          = "style";

    /** 表示表单控件标签是否可用的属性名称 - {@code disabled}。 */
    public static final String DISABLED       = "disabled";

    /** 表示表单控件标签类型的属性名称 - {@code type}。 */
    public static final String TYPE           = "type";

    /** 表示表单控件标签的值的属性名称 - {@code value}。 */
    public static final String VALUE          = "value";

    /** 规定目标 {@code URL} 的字符编码的属性名称 - {@code charset}。 */
    public static final String CHARSET        = "charset";

    /** 规定链接的坐标的属性名称 - {@code coords}。 */
    public static final String COORDS         = "coords";

    /** 目标 {@code URL} 的属性名称 - {@code href}。 */
    public static final String HREF           = "href";

    /** 目标 {@code URL} 基准语言的属性名称 - {@code hreflang}。 */
    public static final String HREF_LANG      = "hreflang";

    /** 规定当前文档与目标 {@code URL} 之间的关系的属性名称 - {@code rel}。 */
    public static final String REL            = "rel";

    /** 规定目标 {@code URL} 与当前文档之间的关系的属性名称 - {@code rev}。 */
    public static final String REV            = "rev";

    /**
     * 在何处打开目标 {@code URL} 的属性名称 - {@code target}。可选的值有：
     * <ul>
     * <li>{@code _black  } - 新窗口中打开目标 {@code URL}</li>
     * <li>{@code _parent } - 父窗口中打开目标 {@code URL}</li>
     * <li>{@code _self   } - 当前窗口中打开目标 {@code URL}</li>
     * <li>{@code _top    } - 顶部窗口中打开目标 {@code URL}</li>
     * </ul>
     */
    public static final String TARGET         = "target";

    /** 定义文字的方向的属性名称 - {@code dir}。 可选的值：{@code ltr - 从左至右、rtl - 从右至左} */
    public static final String DIR            = "dir";

    /** 定义横向对齐方式的属性名称 - {@code align}。 */
    public static final String ALIGN          = "align";

    /** 定义根据哪个字符来进行文本对齐的属性名称 - {@code char}。<strong>仅当 align="char" 时使用！</strong> */
    public static final String CHAR           = "char";

    /** 定义第一个对齐字符的偏移量的属性名称 - {@code charoff}。<strong>仅当 align="char" 时使用！</strong> */
    public static final String CHAROFF        = "charoff";

    /** 定义 {@code &lt;col&gt;} 应当横跨的列数的属性名称 - {@code span}。 */
    public static final String SPAN           = "span";

    /** 定义纵向对齐方式的属性名称 - {@code valign}。 */
    public static final String VALIGN         = "valign";

    /** 当确认按钮被按下时，接受和处理表单数据的 {@code URL} 的属性名称 - {@code action}。 */
    public static final String ACTION         = "action";

    /** 用于向 action URL 传送数据的 HTTP 方法的属性名称 - {@code method}。它的默认值为 <strong>get</strong> */
    public static final String METHOD         = "method";

    /** 一个服务器可接受的字符集的列表（用逗号分隔）的属性名称 - {@code accept-charset}。 */
    public static final String ACCEPT_CHARSET = "accept-charset";

    /** 用来编码表单内容的 MIME 类型的属性名称 - {@code enctype}。 */
    public static final String ENCTYPE        = "enctype";

    /**
     * {@link HtmlTags#IMAGE}标签要显示的图片的 {@code URL} 或 {@link HtmlTags#SCRIPT}
     * 标签要加载的 Javascript {@code URL} 的属性名称 - {@code src}。
     */
    public static final String SRC            = "src";

    /** 一个空格分隔的指向档案文件的 {@code URL} 列表的属性名称 - {@code archive}。使用标签：{@link HtmlTags#OBJECT} */
    public static final String ARCHIVE        = "archive";

    /**
     * 定义嵌入 Windows Registry 中或某个 {@code URL} 中的类的 ID 值的属性名称 - {@code classid}。使用标签：{@link HtmlTags#OBJECT}
     */
    public static final String CLASSID        = "classid";

    /** 定义在何处可找到对象所需的代码，提供一个基准 {@code URL} 的属性名称 - {@code codebase}。使用标签：{@link HtmlTags#OBJECT} */
    public static final String CODEBASE       = "codebase";

    /**
     * 通过 {@code classid} 属性所引用的代码的 {@code MIME} 类型的属性名称 - {@code codetype}。使用标签：{@link HtmlTags#OBJECT}
     */
    public static final String CODETYPE       = "codetype";

    /**
     * 定义引用对象数据的 {@code URL}。如果有需要对象处理的数据文件,要用 {@code data} 属性来指定这些数据文件的属性名称 -
     * {@code data}。使用标签：{@link HtmlTags#OBJECT}
     */
    public static final String DATA           = "data";

    /**
     * 定义此对象仅可被声明，但不能被创建或例示，直到此对象得到应用为止的属性名称 - {@code declare}。使用标签：{@link HtmlTags#OBJECT}
     */
    public static final String DECLARE        = "declare";

    /** 定义当对象正在加载时所显示的文本的属性名称 - {@code standby}。使用标签：{@link HtmlTags#OBJECT} */
    public static final String STANDBY        = "standby";

    /** 定义周围水平方向的空白的属性名称 - {@code hspace}。 */
    public static final String HSPACE         = "hspace";

    /** 定义垂直方向的空白的属性名称 - {@code vspace}。 */
    public static final String VSPACE         = "vspace";

    /** 定义周围的边框的属性名称 - {@code border}。 */
    public static final String BORDER         = "border";

    /** 定义选项组的标记的属性名称 - {@code label}。 */
    public static final String LABEL          = "label";

    /** 规定文本区内可见的列数的属性名称 - {@code cols}。使用标签：{@link HtmlTags#TEXTAREA} */
    public static final String COLS           = "cols";

    /** 规定文本区内可见的行数的属性名称 - {@code rows}。使用标签：{@link HtmlTags#TEXTAREA} */
    public static final String ROWS           = "rows";

    /** 表示控件为只读的属性名称 - {@code readonly}。 */
    public static final String READONLY       = "readonly";
}